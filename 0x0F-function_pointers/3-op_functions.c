#include <stdlib.h>
#include "math.h"
#include "3-calc.h"




/**
 * op_add - adds two numbers
 *
 * @a: first number
 * @b: second number
 *
 * Return: return sum of numbers
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - subtracts two numbers
 *
 * @a: first number
 * @b: second number
 *
 * Return: return difference of numbers
 */

int op_sub(int a, int b)
{
	return (a - b);
}


/**
 * op_mul - multiplies two numbers
 *
 * @a: first number
 * @b: second number
 *
 * Return: return value of two numbers
 */

int op_mul(int a, int b)
{
	return (a * b);
}



/**
 * op_div - divides two numbers
 *
 * @a: first number
 * @b: second number
 *
 * Return: quotient of two numbers
 */

int op_div(int a, int b)
{
	return (a / b);
}


/**
 * op_mod - remainder of the division of two numbers
 *
 * @a: first number
 * @b: second number
 *
 * Return: return remainder of two numbers
 */
int op_mod(int a, int b)
{
	return (a % b);
}

