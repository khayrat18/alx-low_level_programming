#include <string.h>

/**
 * print_diagsums - prints the sum of the two diagonals
 *
 * @a: character to be checked
 * @size: character to be checked
 *
 * Return: return value
 */

void print_diagsums(int *a, int size)
{

int i;
int sum;
sum = 0;

for (i = 0; i < size; i++)
{
sum = sum + a[i];
}
return (0);

}
